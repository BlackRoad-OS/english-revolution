    print("      im-POR-tant ⭐")
    print("      re-MEM-ber 🧠")
    print("      to-MOR-row 📅")

    print("\n💡 VISUAL STRESS:")
    print("   📢 = BIG = louder, longer, higher pitch")
    print("   🤫 = small = quieter, shorter, lower pitch")

    print("\n" + "="*70)
    print("SENTENCE RHYTHM")
    print("="*70)

    print("\n📝 English sentences have rhythm!")
    print("   Content words = STRESSED 📢")
    print("   Function words = unstressed 🤫")

    print("\n✅ EXAMPLE:")
