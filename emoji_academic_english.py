def paraphrasing():
    """Paraphrase without plagiarizing"""
    print("\n" + "="*70)
    print("🔄 PARAPHRASING vs PLAGIARISM")
    print("="*70)

    print("\n💡 WHAT IS PARAPHRASING?")
    print("   Restating someone else's idea in YOUR words")
    print("   Still cite the source!")

    print("\n" + "="*70)
    print("🎯 HOW TO PARAPHRASE")
    print("="*70)

    print("\n   STEP 1: Read and understand")
    print("   STEP 2: Put it away (don't look while writing!)")
    print("   STEP 3: Write in YOUR words")
    print("   STEP 4: Check - is it different enough?")
    print("   STEP 5: Cite the source")

    print("\n✅ PARAPHRASING TECHNIQUES:")

    print("\n   1️⃣ CHANGE VOCABULARY:")
    print("   Original: 'Climate change is accelerating'")
    print("   Paraphrase: 'Global warming is speeding up'")

    print("\n   2️⃣ CHANGE SENTENCE STRUCTURE:")
    print("   Original: 'Students who study regularly perform better'")
    print("   Paraphrase: 'Regular study leads to better performance'")

    print("\n   3️⃣ CHANGE VOICE:")
    print("   Original: 'The researchers conducted experiments'")
    print("   Paraphrase: 'Experiments were conducted'")

    print("\n   4️⃣ COMBINE SENTENCES:")
    print("   Original: 'Exercise is important. It improves health.'")
    print("   Paraphrase: 'Exercise is important for health improvement'")

    print("\n" + "="*70)
    print("❌ PLAGIARISM EXAMPLES")
    print("="*70)

    print("\n📖 ORIGINAL:")
    print("   'Social media has fundamentally changed the way")
    print("    people communicate in modern society.'")

    print("\n❌ PLAGIARISM (Just changing few words):")
    print("   'Social media has basically changed the way people")
    print("    communicate in modern times.'")
    print("   ← TOO SIMILAR! This is plagiarism!")

    print("\n✅ PROPER PARAPHRASE:")
    print("   'Modern communication has been transformed by")
    print("    the rise of social networking platforms'")
    print("   (Author, Year).")
    print("   ← Different structure + vocabulary + CITED!")

    print("\n" + "="*70)
    print("🎯 WHEN TO QUOTE vs PARAPHRASE")
    print("="*70)

    print("\n✅ USE DIRECT QUOTE when:")
    print("   - Author's exact words are powerful/memorable")
    print("   - Defining a term specifically")
    print("   - Need precise wording (law, policy)")

    print("\n✅ PARAPHRASE when:")
    print("   - General information")
    print("   - Simplifying complex idea")
    print("   - Integrating smoothly into your writing")
    print("   - Most of the time! (Paraphrase > Quote)")


