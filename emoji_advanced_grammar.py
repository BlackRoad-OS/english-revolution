    print("\n🎯 COULD HAVE:")
    print("   (Past ability/possibility - but didn't happen!)")

    print("\n   📝 FORMULA:")
    print("   COULD + HAVE + PAST PARTICIPLE")

    print("\n   🎯 MEANING #1: Had ability but didn't do it")
    print("   ✅ EXAMPLES:")
    print("   - 'I could have helped you' (I was able, but I didn't)")
    print("   - 'She could have won' (she was capable, but didn't)")
    print("   - 'We could have gone to the party' (possible, didn't go)")

    print("\n   🎯 MEANING #2: Past possibility (speculating)")
    print("   ✅ EXAMPLES:")
    print("   - 'He could have forgotten' (maybe he forgot)")
    print("   - 'It could have been an accident' (maybe)")
    print("   - 'She could have left already' (possibly)")

    print("\n🎯 WOULD HAVE:")
    print("   (Imaginary past - hypothetical)")

    print("\n   📝 FORMULA:")
    print("   WOULD + HAVE + PAST PARTICIPLE")

    print("\n   🎯 MEANING: What would happen in imaginary past")
    print("   💡 Used in Third Conditional!")
    print("\n   ✅ EXAMPLES:")
    print("   - 'I would have gone if I had known'")
    print("     (I didn't know, so I didn't go)")
    print("   - 'She would have helped if you had asked'")
    print("     (You didn't ask, she didn't help)")
    print("   - 'They would have succeeded if they had tried'")
    print("     (They didn't try, didn't succeed)")

    print("\n🎯 MIGHT HAVE / MAY HAVE:")
    print("   (Past possibility - less certain than 'could')")

    print("\n   📝 FORMULA:")
    print("   MIGHT/MAY + HAVE + PAST PARTICIPLE")

    print("\n   🎯 MEANING: Perhaps, maybe (about past)")
    print("\n   ✅ EXAMPLES:")
    print("   - 'She might have forgotten' (perhaps she forgot)")
    print("   - 'He may have left already' (possibly)")
    print("   - 'I might have seen him' (not sure)")

    print("\n🎯 MUST HAVE:")
    print("   (Deduction about past - very certain!)")

