def subjunctive_mood():
    """The subjunctive (formal English, wishes, demands)"""

    print("\n" + "="*60)
    print("👑 MODULE 2: SUBJUNCTIVE MOOD")
    print("   Formal English & Wishes (The 'Secret' Grammar!)")
    print("="*60)

    print("\n💡 THE SECRET:")
    print("   Subjunctive = special verb form for:")
    print("   - Wishes, demands, suggestions")
    print("   - Formal/legal language")
    print("   - Fixed expressions")
    print("   💡 Even many natives don't know this!")

    print("\n🎯 TYPE 1: MANDATIVE SUBJUNCTIVE")
    print("   (Demands, suggestions, recommendations)")

    print("\n   📝 FORMULA:")
    print("   [Trigger verb] + THAT + [subject] + BASE VERB")
    print("\n   🔥 KEY: BASE VERB (no 's', no conjugation!)")

    print("\n   ✅ TRIGGER VERBS:")
    print("   - suggest, recommend, propose")
    print("   - demand, require, request, ask")
    print("   - insist, urge")
    print("   - command, order")
    print("   - advise")

    print("\n   ✅ EXAMPLES:")
    print("   - 'I suggest that he GO' (not 'goes')")
    print("   - 'She demands that they BE on time' (not 'are')")
    print("   - 'The doctor recommends that she TAKE medicine' (not 'takes')")
    print("   - 'It's essential that you BE there' (not 'are')")
    print("\n   💡 NOTICE: No 's', no 'is/are', just BASE VERB!")

    print("\n   ✅ ADJECTIVES that trigger subjunctive:")
    print("   - It's important that...")
    print("   - It's essential that...")
    print("   - It's necessary that...")
    print("   - It's crucial that...")
    print("   - It's vital that...")
    print("   - It's imperative that...")
    print("\n   'It's important that he BE here' (not 'is')")

    print("\n🎯 TYPE 2: FORMULAIC SUBJUNCTIVE")
    print("   (Fixed expressions)")

    print("\n   ✅ COMMON EXPRESSIONS:")
    print("   - 'God save the Queen!' (not 'saves')")
    print("   - 'Long live the King!' (not 'lives')")
    print("   - 'Heaven forbid!' (not 'forbids')")
    print("   - 'Be that as it may...'")
    print("   - 'Come what may...'")
    print("   - 'If need be...'")
    print("   - 'Suffice it to say...'")

    print("\n🎯 TYPE 3: 'WERE' SUBJUNCTIVE")
    print("   (Hypothetical situations)")

    print("\n   📝 FORMULA:")
    print("   IF + subject + WERE (for all persons)")
    print("\n   ✅ EXAMPLES:")
    print("   - 'If I were you...' (not 'was')")
    print("   - 'If she were here...' (not 'was')")
    print("   - 'If it were possible...' (not 'was')")
    print("   - 'I wish I were taller' (not 'was')")
    print("\n   💡 'WERE' for ALL persons in hypotheticals!")

    print("\n🎯 TYPE 4: WISHES & REGRETS")
    print("   Special subjunctive patterns!")

    print("\n   📝 WISH + PAST = Present/Future wish")
    print("   (Want something different NOW)")
    print("   ✅ EXAMPLES:")
    print("   - 'I wish I were rich' (I'm not rich)")
    print("   - 'I wish I had more time' (I don't have time)")
    print("   - 'I wish she were here' (she's not here)")

    print("\n   📝 WISH + PAST PERFECT = Past regret")
    print("   (Want the PAST to be different)")
    print("   ✅ EXAMPLES:")
    print("   - 'I wish I had studied harder' (regret!)")
    print("   - 'I wish I hadn't said that' (regret!)")
    print("   - 'I wish we had met earlier' (regret!)")

    print("\n   📝 WISH + WOULD = Annoying habit")
    print("   (Want someone to change behavior)")
    print("   ✅ EXAMPLES:")
    print("   - 'I wish you would stop smoking'")
    print("   - 'I wish he would listen'")
    print("   - 'I wish it would stop raining'")

    print("\n🔥 'WOULD RATHER' SUBJUNCTIVE:")

    print("\n   📝 Would rather + subject + PAST")
    print("   (Preference for present/future)")
    print("   ✅ EXAMPLES:")
    print("   - 'I'd rather you didn't smoke here'")
    print("   - 'I'd rather she came tomorrow' (not 'comes')")
    print("   - 'I'd rather they were here' (not 'are')")

    print("\n   📝 Would rather + PAST PERFECT")
    print("   (Preference about past)")
    print("   ✅ EXAMPLES:")
    print("   - 'I'd rather you hadn't told him'")
    print("   - 'I'd rather we had stayed home'")

    print("\n🔥 'IT'S TIME' SUBJUNCTIVE:")

    print("\n   📝 It's time + subject + PAST")
    print("   ✅ EXAMPLES:")
    print("   - 'It's time we left' (not 'leave')")
    print("   - 'It's time you went to bed' (not 'go')")
    print("   - 'It's high time he got a job' (not 'gets')")
    print("\n   💡 Use PAST even though talking about NOW/FUTURE!")

    print("\n🎯 SUBJUNCTIVE vs INDICATIVE:")

    print("\n   SUBJUNCTIVE (hypothetical, formal):")
    print("   - 'I suggest that he BE here'")
    print("   - 'It's important that she GO'")
    print("   - 'I wish I WERE taller'")

    print("\n   INDICATIVE (factual, normal):")
    print("   - 'I think that he IS here'")
    print("   - 'I know that she GOES'")
    print("   - 'I know I AM short'")

    print("\n💡 COMMON MISTAKES:")

    print("\n   ❌ WRONG: 'I suggest that he goes'")
    print("   ✅ RIGHT: 'I suggest that he go'")

    print("\n   ❌ WRONG: 'It's important that she is there'")
    print("   ✅ RIGHT: 'It's important that she be there'")

    print("\n   ❌ WRONG: 'If I was you'")
    print("   ✅ RIGHT: 'If I were you'")

    print("\n   ❌ WRONG: 'I wish I was rich'")
    print("   ✅ RIGHT: 'I wish I were rich' (formal)")
    print("   ⚠️ NOTE: 'was' accepted in casual speech!")

    print("\n🔥 FORMAL vs INFORMAL:")

    print("\n   FORMAL (subjunctive):")
    print("   - 'I suggest that he be fired'")
    print("   - 'It's essential that she attend'")
    print("   - 'I insist that they come'")

    print("\n   INFORMAL (alternative):")
    print("   - 'I suggest he should be fired'")
    print("   - 'It's essential for her to attend'")
    print("   - 'I insist they should come'")

    print("\n   💡 SUBJUNCTIVE = More formal/American")
    print("   💡 SHOULD = More common/British")

    input("\n[Press Enter to continue...]")


def perfect_tenses():
    """Present perfect, past perfect, future perfect (time relationships!)"""

    print("\n" + "="*60)
    print("⏰ MODULE 3: PERFECT TENSES MASTERY")
    print("   Time Relationships (Visual Timeline!)")
    print("="*60)

    print("\n💡 THE SECRET:")
    print("   Perfect tenses show RELATIONSHIP between times!")
    print("   NOT just 'when' but 'when compared to when'!")
    print("   💡 VISUAL timelines make this EASY!")

    print("\n🎯 PRESENT PERFECT (Past → Present connection):")

    print("\n   📝 FORMULA:")
    print("   HAVE/HAS + PAST PARTICIPLE")

    print("\n   🎯 USE #1: Action in past, result/relevance NOW")
    print("   Timeline: [PAST ACTION] ━━━━━ [PRESENT RESULT]")
    print("\n   ✅ EXAMPLES:")
    print("   - 'I've lost my keys' (past action, can't find them NOW)")
    print("   - 'She's broken her leg' (past break, still broken NOW)")
    print("   - 'They've moved to Paris' (past move, in Paris NOW)")

    print("\n   🎯 USE #2: Experience up to now (life experience)")
    print("   Timeline: [BIRTH] ━━━━━ [EXPERIENCES] ━━━━━ [NOW]")
    print("\n   ✅ EXAMPLES:")
    print("   - 'I've been to Japan three times'")
    print("   - 'Have you ever eaten sushi?'")
    print("   - 'She's never seen snow'")

    print("\n   🎯 USE #3: Ongoing situation (started past, continues now)")
    print("   Timeline: [STARTED PAST] ━━━━━━━━━━━━━━━ [NOW]")
    print("\n   ✅ EXAMPLES:")
    print("   - 'I've lived here for 5 years' (still live here)")
    print("   - 'He's worked here since 2010' (still works here)")
    print("   - 'We've known each other since childhood'")

    print("\n   🎯 USE #4: Recent past (just, recently, lately)")
    print("   ✅ EXAMPLES:")
    print("   - 'I've just finished'")
    print("   - 'She's recently moved'")
    print("   - 'Have you seen him lately?'")

    print("\n   🔥 KEY TIME MARKERS:")
    print("   ✅ FOR: duration (for 5 years, for 3 months)")
    print("   ✅ SINCE: starting point (since 2010, since Monday)")
    print("   ✅ YET: in questions/negatives (Have you finished yet?)")
    print("   ✅ ALREADY: earlier than expected (I've already eaten)")
    print("   ✅ JUST: very recently (I've just arrived)")
    print("   ✅ EVER: at any time (Have you ever...?)")
    print("   ✅ NEVER: at no time (I've never seen...)")

    print("\n🎯 PAST PERFECT (Earlier Past → Later Past):")

    print("\n   📝 FORMULA:")
    print("   HAD + PAST PARTICIPLE")

    print("\n   🎯 USE: One past action BEFORE another past action")
    print("   Timeline: [PAST PERFECT] ━━━ [SIMPLE PAST] ━━━ [NOW]")
    print("\n   ✅ EXAMPLES:")
    print("   - 'When I arrived, the train had already left'")
    print("     (Left BEFORE I arrived - both past)")
    print("\n   - 'She had never seen snow before she moved to Canada'")
    print("     (Never seen BEFORE moving - both past)")
    print("\n   - 'I had finished my homework when he called'")
    print("     (Finished BEFORE he called - both past)")

    print("\n   💡 SHOWS: Which past event happened FIRST!")

    print("\n🎯 FUTURE PERFECT (Now → Future point):")

    print("\n   📝 FORMULA:")
    print("   WILL HAVE + PAST PARTICIPLE")

    print("\n   🎯 USE: Action completed BEFORE future time")
    print("   Timeline: [NOW] ━━━ [COMPLETE] ━━━ [FUTURE POINT]")
    print("\n   ✅ EXAMPLES:")
    print("   - 'By 2025, I will have finished university'")
    print("     (Finish happens BEFORE 2025)")
    print("\n   - 'By the time you arrive, I'll have cooked dinner'")
    print("     (Cook BEFORE you arrive)")
    print("\n   - 'They will have left before we get there'")
    print("     (Leave BEFORE we get there)")

    print("\n   🔥 KEY TIME MARKERS:")
    print("   ✅ BY: by next year, by tomorrow, by the time...")
    print("   ✅ BEFORE: before he arrives, before 2025")

    print("\n🎯 PERFECT CONTINUOUS (Duration emphasis!):")

    print("\n   📝 PRESENT PERFECT CONTINUOUS:")
    print("   HAVE/HAS + BEEN + -ING")
    print("\n   🎯 USE: Ongoing action, emphasis on DURATION")
    print("   ✅ EXAMPLES:")
    print("   - 'I've been studying for 3 hours' (still studying)")
    print("   - 'She's been working here since 2010' (still working)")
    print("   - 'It's been raining all day' (still raining)")

    print("\n   📝 PAST PERFECT CONTINUOUS:")
    print("   HAD + BEEN + -ING")
    print("\n   🎯 USE: Ongoing action BEFORE past point")
    print("   ✅ EXAMPLES:")
    print("   - 'I had been waiting for an hour when she arrived'")
    print("   - 'They had been living there for 10 years before they moved'")

    print("\n   📝 FUTURE PERFECT CONTINUOUS:")
    print("   WILL + HAVE + BEEN + -ING")
    print("\n   🎯 USE: Ongoing action continuing until future point")
    print("   ✅ EXAMPLES:")
    print("   - 'By December, I'll have been working here for 5 years'")
    print("   - 'By the time you arrive, I'll have been cooking for hours'")

    print("\n🔥 PERFECT vs PERFECT CONTINUOUS:")

    print("\n   PERFECT (completed/result focus):")
    print("   - 'I've read the book' (finished)")
    print("   - 'I've painted the room' (complete)")

    print("\n   PERFECT CONTINUOUS (duration/process focus):")
    print("   - 'I've been reading all day' (duration)")
    print("   - 'I've been painting' (ongoing)")

    print("\n🎯 COMMON MISTAKES:")

    print("\n   ❌ MISTAKE #1: Using present perfect with specific past time")
    print("   ❌ WRONG: 'I've seen him yesterday'")
    print("   ✅ RIGHT: 'I saw him yesterday' (use simple past!)")
    print("   💡 Yesterday/last week/in 2010 = SIMPLE PAST!")

    print("\n   ❌ MISTAKE #2: Confusing FOR and SINCE")
    print("   ❌ WRONG: 'I've lived here since 5 years'")
    print("   ✅ RIGHT: 'I've lived here for 5 years'")
    print("   ✅ RIGHT: 'I've lived here since 2019'")
    print("   💡 FOR = duration, SINCE = starting point!")

    print("\n   ❌ MISTAKE #3: Missing HAVE in present perfect")
    print("   ❌ WRONG: 'I been there'")
    print("   ✅ RIGHT: 'I have been there' or 'I've been there'")

    print("\n   ❌ MISTAKE #4: Using past perfect when not needed")
    print("   ❌ WRONG: 'I had eaten breakfast this morning'")
    print("   ✅ RIGHT: 'I ate breakfast this morning'")
    print("   💡 Only use past perfect for 'past before past'!")

    input("\n[Press Enter to continue...]")


