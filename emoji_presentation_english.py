    print("🎯 Q&A STRUCTURE")
    print("="*70)

    print("\n📝 THE FORMULA:")
    print("   1. LISTEN fully (don't interrupt)")
    print("   2. PAUSE (2 seconds to think)")
    print("   3. REPHRASE question (ensure understanding)")
    print("   4. ANSWER concisely")
    print("   5. CHECK if answered ('Does that answer your question?')")

    print("\n✅ EXAMPLE:")
    print("   QUESTION: 'What about remote teams? Does this work for them?'")
    print()
    print("   YOU:")
    print("   [PAUSE 2 seconds]")
    print("   'Great question. You're asking if this approach works")
    print("    for distributed teams, right?'")
    print("   [They nod]")
    print("   'Absolutely. Here's how: [answer]'")
    print("   'Does that address your question?'")

    print("\n💡 WHY THIS WORKS:")
    print("   Listen = respect")
    print("   Pause = confidence (not scrambling!)")
    print("   Rephrase = ensures understanding + buys time!")
    print("   Answer = deliver value")
    print("   Check = care about helping")

