def outline_method():
    """📝 OUTLINE METHOD - Hierarchical Structure!"""

    print("\n" + "="*60)
    print("📝 OUTLINE METHOD")
    print("="*60)

    print("\n💡 WHAT IS THE OUTLINE METHOD?")
    print("   Traditional note-taking with hierarchy")
    print("   Uses indentation to show relationships")
    print("   Main topics → Subtopics → Details")
    print("   Simple, fast, works for most situations")

    print("\n📐 OUTLINE STRUCTURE:")

    print("\n   I. MAIN TOPIC (far left)")
    print("      A. Subtopic (indented)")
    print("         1. Detail (more indented)")
    print("            a. Sub-detail (even more indented)")
    print("            b. Sub-detail")
    print("         2. Detail")
    print("      B. Subtopic")
    print("   II. MAIN TOPIC")

    print("\n📝 EXAMPLE: American Revolution")

    print("\n   I. Causes of American Revolution")
    print("      A. Taxation without representation")
    print("         1. Stamp Act (1765)")
    print("            a. Tax on printed materials")
    print("            b. Colonists had no say")
    print("         2. Tea Act (1773)")
    print("            a. Tax on tea")
    print("            b. Led to Boston Tea Party")
    print("      B. British military presence")
    print("         1. Quartering Act")
    print("         2. Colonists forced to house soldiers")
    print("   II. Key Events")
    print("      A. Boston Tea Party (1773)")
    print("      B. First Continental Congress (1774)")

    print("\n✍️ HOW TO USE OUTLINE METHOD:")

    print("\n   📝 DURING LECTURE:")
    print("   1. Listen for main topics (I, II, III)")
    print("   2. Listen for subtopics (A, B, C)")
    print("   3. Fill in details (1, 2, 3)")
    print("   4. Add sub-details as needed (a, b, c)")

    print("\n   💡 SIGNALS FOR MAIN TOPICS:")
    print("   - 'First...' / 'Second...' / 'Finally...'")
    print("   - 'There are three main causes...'")
    print("   - New slide or chapter")
    print("   - Teacher says 'This is important'")

    print("\n   🎯 ABBREVIATIONS TO USE:")
    print("   - w/ = with")
    print("   - w/o = without")
    print("   - b/c = because")
    print("   - → = leads to, causes, results in")
    print("   - ∴ = therefore")
    print("   - = = equals, is the same as")
    print("   - ≠ = is different from")
    print("   - ↑ = increase")
    print("   - ↓ = decrease")
    print("   - ex: = example")

    print("\n📱 DIGITAL OUTLINING:")

    print("\n   ✅ ADVANTAGES:")
    print("   - Easy to reorganize (drag & drop)")
    print("   - Fast typing (if you type fast)")
    print("   - Searchable")
    print("   - Never run out of space")

    print("\n   ✍️ TOOLS:")
    print("   - Microsoft Word (bullet points + Tab key)")
    print("   - Google Docs (same)")
    print("   - Notion (collapsible sections)")
    print("   - Obsidian (markdown outlining)")
    print("   - Paper (still works perfectly!)")

    print("\n🎯 WHEN TO USE OUTLINE METHOD:")

    print("\n   ✅ PERFECT FOR:")
    print("   - Fast-paced lectures (can keep up)")
    print("   - Structured presentations")
    print("   - Textbook reading")
    print("   - History, literature, social sciences")
    print("   - Meeting notes")
    print("   - ANY subject with clear hierarchy")

    print("\n   ❌ NOT IDEAL FOR:")
    print("   - Visual subjects (use mind maps)")
    print("   - Math problems (use problem-solution format)")
    print("   - Brainstorming (mind maps better)")

    print("\n💡 OUTLINE METHOD TIPS:")

    print("\n   🎯 TIP 1: DON'T OVERTHINK HIERARCHY")
    print("   Roman numerals (I, II) get confusing after XII")
    print("   Just use: Main → Sub → Detail → Sub-detail")
    print("   Or even simpler: -, •, ○, ▪")

    print("\n   🎯 TIP 2: USE CONSISTENT INDENTATION")
    print("   Same level = same indentation")
    print("   Makes relationships clear at a glance")

    print("\n   🎯 TIP 3: LEAVE SPACE")
    print("   Skip lines between main topics")
    print("   Easier to add info later")
    print("   Easier to read when reviewing")

    print("\n   🎯 TIP 4: ADD SYMBOLS FOR PRIORITY")
    print("   ⭐ = will be on exam")
    print("   ❓ = confused, need to review")
    print("   ❗ = important concept")
    print("   📝 = homework related")

    print("\n🔄 COMBINING WITH CORNELL:")

    print("\n   You can use OUTLINE METHOD in the notes column")
    print("   of CORNELL NOTES!")

    print("\n   ┌──────────┬──────────────────────────┐")
    print("   │ What are │ I. Causes                │")
    print("   │ causes?  │    A. Taxation           │")
    print("   │          │       1. Stamp Act       │")
    print("   │ Key      │    B. Military           │")
    print("   │ events?  │ II. Key Events           │")
    print("   │          │    A. Boston Tea Party   │")
    print("   └──────────┴──────────────────────────┘")

    print("\n   = Best of both worlds!")

    print("\n💪 OUTLINE METHOD ADVANTAGES:")
    print("   ✅ Fast (keep up with lectures)")
    print("   ✅ Clear hierarchy (see relationships)")
    print("   ✅ Flexible (works for any subject)")
    print("   ✅ Simple (no fancy tools needed)")
