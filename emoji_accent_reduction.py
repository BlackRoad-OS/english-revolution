    print("   The Music of English (Statement vs Question!)")
    print("="*60)

    print("\n💡 THE SECRET:")
    print("   Intonation = pitch going UP ↗️ or DOWN ↘️")
    print("   Same words, different pitch = DIFFERENT meaning!")
    print("   💡 'You're leaving?' ↗️ (question) vs 'You're leaving' ↘️ (statement)")

    print("\n🎯 THE 3 BASIC PATTERNS:")

    print("\n1️⃣ FALLING INTONATION ↘️:")
    print("   When to use: Statements, commands, Wh-questions")

    print("\n   ✅ STATEMENTS:")
    print("   'I'm going home' ↘️")
    print("   'She likes coffee' ↘️")
    print("   'The meeting is tomorrow' ↘️")
    print("   💡 Voice goes DOWN at end = finality!")

    print("\n   ✅ COMMANDS:")
    print("   'Sit down' ↘️")
    print("   'Close the door' ↘️")
    print("   'Be quiet' ↘️")
    print("   💡 DOWN = authority, definiteness!")

    print("\n   ✅ WH-QUESTIONS (who, what, where, when, why, how):")
    print("   'Where are you going?' ↘️")
    print("   'What time is it?' ↘️")
    print("   'How are you?' ↘️")
    print("   💡 DOWN = genuine question, expecting answer!")

    print("\n2️⃣ RISING INTONATION ↗️:")
    print("   When to use: Yes/No questions, uncertainty")

    print("\n   ✅ YES/NO QUESTIONS:")
    print("   'Are you ready?' ↗️")
    print("   'Do you like pizza?' ↗️")
    print("   'Is she coming?' ↗️")
    print("   💡 UP = question, expecting yes/no!")

    print("\n   ✅ UNCERTAINTY/CHECKING:")
    print("   'You're leaving?' ↗️ (really?)")
    print("   'It's 5 o'clock?' ↗️ (are you sure?)")
    print("   'This one?' ↗️ (checking)")
    print("   💡 UP = not sure, need confirmation!")

    print("\n   ✅ LISTS (Incomplete):")
    print("   'I need milk ↗️, eggs ↗️, and bread ↘️'")
    print("   💡 UP on each item EXCEPT last (which goes DOWN)!")

    print("\n3️⃣ FALL-RISE INTONATION ↘️↗️:")
    print("   When to use: Contrast, politeness, implications")

    print("\n   ✅ CONTRAST:")
    print("   'I like coffee ↘️↗️... but not tea'")
    print("   'It's good ↘️↗️... but expensive'")
    print("   💡 Implies: 'There's more to say!'")

    print("\n   ✅ POLITE DISAGREEMENT:")
    print("   'Well ↘️↗️... I'm not sure about that'")
    print("   'Actually ↘️↗️... that's not quite right'")
    print("   💡 Soften the disagreement!")

    print("\n   ✅ IMPLIED MEANING:")
    print("   'It's fine ↘️↗️' (but not great)")
    print("   'I could do that ↘️↗️' (but I don't want to)")
    print("   💡 Subtext! Something left unsaid!")

    print("\n🔥 MEANING CHANGES WITH INTONATION:")

    print("\n   Example: 'She's coming'")
    print("\n   ↘️ 'She's coming' = Statement (she will come)")
    print("   ↗️ 'She's coming?' = Question (is she coming?)")
    print("   ↘️↗️ 'She's coming' = Implication (she is, but maybe shouldn't)")

    print("\n   Example: 'Really'")
    print("\n   ↘️ 'Really' = Strong agreement (yes, really!)")
    print("   ↗️ 'Really?' = Surprise/question (are you sure?)")
    print("   ↘️↗️ 'Really' = Sarcastic (I don't believe you)")

    print("\n🎯 SENTENCE STRESS + INTONATION:")

    print("\n   📝 NORMAL SENTENCE:")
    print("   'I didn't say YOU stole my MONey' ↘️")
    print("   (Stress on 'you' and 'money')")

    print("\n   📝 EMPHASIZING DIFFERENT WORDS:")
    print("   'I DIDN'T say you stole my money' ↘️")
    print("   (Someone else did, not me!)")

    print("\n   'I didn't SAY you stole my money' ↘️")
    print("   (I implied it, didn't say it!)")

    print("\n   'I didn't say YOU stole my money' ↘️")
    print("   (Someone else stole it!)")

    print("\n   'I didn't say you STOLE my money' ↘️")
    print("   (You borrowed it!)")

    print("\n   'I didn't say you stole MY money' ↘️")
    print("   (You stole someone else's!)")

    print("\n   💡 STRESS + INTONATION = MEANING!")

    print("\n🎯 HOW TO PRACTICE:")

